# import - usado para trazer modulos e pacotes externos ou internos para o codigo no arquivo em que for usado,
# permitindo a utilizacao de funcoes, classes e variaveis definidas nesses modulos.

import math

# pi - valor constante de pi

print(math.pi)

# ceil e floor - arredondamento para o inteiro mais proximo

num_decimal = 3.7

print(math.ceil(num_decimal)) # arredonda para o inteiro maior
print(math.floor(num_decimal)) # arredonda para o inteiro menor

# sqrt - obter raiz quadrada de um numero

print(math.sqrt(16))
