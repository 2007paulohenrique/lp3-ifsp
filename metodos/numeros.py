numero1 = 15
numero2 = -42

# abs - valor absoluto

print(abs(numero2))

# pow - inteiro elevado a uma potencia

print(pow(numero1, 2))  

# numero elevado a uma potencia

print(numero1 ** 2)

# round - numero arredondado, com ou sem uma quantidade especifica de casas decimais

num_decimal = 3.14159

print(round(num_decimal))
print(round(num_decimal, 2))

# conversao entre tipos numericos, caso seja possivel

num_inteiro = int(3.14)  # converte de float para int
num_float = float(7)    # converte de int para float

print(num_inteiro)
print(num_float)
