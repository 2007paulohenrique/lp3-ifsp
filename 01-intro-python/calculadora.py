def somar(num1, num2):
    return num1 + num2

def subtrair(num1, num2):
    return num1 - num2

def multiplicar(num1, num2):
    return num1 * num2

def dividir(num1, num2):
    return num1/num2

def raiz(num, indice):
    return num**(1/indice)

def potencia(num, expoente):
    return num**expoente

